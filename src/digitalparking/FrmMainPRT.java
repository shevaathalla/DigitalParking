    private void tampil() {
        try {
            con = digitalparking.koneksi.koneksiDB();
            st = con.createStatement();
            rs = st.executeQuery("select nopol,tipe,jam_masuk from data_parkir");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    public void hapus() {
        try {
            con = digitalparking.koneksi.koneksiDB();
            String sql = "delete from data_parkir where id = '" + jLabel3.getText() + "'";
            pst = con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Barang Dihapus");
            tampil();
        } catch (Exception e) {
        }
    }

