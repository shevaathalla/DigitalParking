    private void update () {
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        try {
            String update = "update data_parkir set jam_keluar= '" + timestamp+ "' where id = " + jLabel3.getText();
            con = digitalparking.koneksi.koneksiDB();
            st = con.createStatement();
            PreparedStatement pst_updet = con.prepareStatement(update);
            pst_updet.execute();
            JOptionPane.showMessageDialog(null, "Data Barang Berhasil diubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    class UppercaseDocumentFilter extends DocumentFilter {
        @Override
        public void insertString(DocumentFilter.FilterBypass fb, int offset, String text, AttributeSet attr) throws BadLocationException {
            fb.insertString(offset, text.toUpperCase(), attr);
        }

        @Override
        public void replace(DocumentFilter.FilterBypass fb, int offset, int length, String text, AttributeSet attrs) throws BadLocationException {
            fb.replace(offset, length, text.toUpperCase(), attrs);
        }
    }

