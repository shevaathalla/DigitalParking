    private int checkOut() {
        updateJamKeluar();
        int id = Integer.parseInt(txtSearch.getText());
        Data dataOut = theHash.find(id);
        int biaya = 0, start = 0, selisih;
        LocalDateTime enter, exit;
        int enterJam, enterMenit, exitJam, exitMenit;
        String blok = dataOut.getBlok();
        switch (blok) {
            case "A":
                start = 5000;
                break;
            case "B":
                start = 4000;
                break;
            case "C":
                start = 3000;
                break;
            case "D":
                start = 3000;
                break;
        }
        enter = dataOut.timeMasuk();
        exit = dataOut.timeKeluar();
        enterJam = enter.getHour();
        enterMenit = enter.getMinute();
        exitJam = exit.getHour();
        exitMenit = exit.getMinute();
        if (enterMenit > 45 && enterMenit <= 59) {
            enterJam++;
        }
        if (exitMenit > 45 && exitMenit <= 59) {
            exitJam++;
        }

        selisih = exitJam - enterJam;
        if (selisih < 2) {
            return start;
        }
        switch (blok) {
            case "A":
                biaya = start + (selisih * 4000);
                break;
            case "B":
                biaya = start + (selisih * 3000);
                break;
            case "C":
                biaya = start + (selisih * 2000);
                break;
            case "D":
                biaya = start + (selisih * 2000);
                break;
        }
        return biaya;
    }

