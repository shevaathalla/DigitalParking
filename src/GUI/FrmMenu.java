    private void load() {
        try {
            ObjectInputStream is = new ObjectInputStream(new FileInputStream("data.bin"));
            Data[] inData = (Data[]) is.readObject();
//            JOptionPane.showMessageDialog(null, inData);
            for (Data inData1 : inData) {
//                System.out.println(inData1);
                if (inData1 != null) {
                    theHeap.insert(inData1, inData1.getBiaya());
                }
            }
            JOptionPane.showMessageDialog(null, "Read Successfully");
            is.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
    }
